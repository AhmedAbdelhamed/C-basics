#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    printf(" sqrt(3)      : %f\n",sqrt(3));
    printf(" exp(3)       : %f\n",exp(3));
    printf(" log(3)       : %f\n",log(3));
    printf(" log10(3)     : %f\n",log10(3));
    printf(" fabs(-3.2)   : %f\n",fabs(-3.2));
    printf(" ceil(3.2)    : %f\n",ceil(3.2));
    printf(" floor(3.2)   : %f\n",floor(3.2));
    printf(" pow(3,2)     : %f\n",pow(3,2));
    printf(" sin(0)       : %f\n",sin(0));
    printf(" cos(90)      : %f\n",cos(90));

    return 0;
}
